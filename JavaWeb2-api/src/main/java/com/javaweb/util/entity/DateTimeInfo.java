package com.javaweb.util.entity;

public class DateTimeInfo {
	
	private Integer year;
	
	private Integer yearOfMonth;
	
	private Integer dayOfMonth;
	
	private Integer dayOfWeek;
	
	private Integer hourOfDay;
	
	private Integer minuteOfHour;
	
	private Integer secondOfMinute;
	
	private Integer totalDayOfMonth;
	
	private Boolean isLeapYear;

	public Integer getYear() {
		return year;
	}

	public void setYear(Integer year) {
		this.year = year;
	}

	public Integer getYearOfMonth() {
		return yearOfMonth;
	}

	public void setYearOfMonth(Integer yearOfMonth) {
		this.yearOfMonth = yearOfMonth;
	}

	public Integer getDayOfMonth() {
		return dayOfMonth;
	}

	public void setDayOfMonth(Integer dayOfMonth) {
		this.dayOfMonth = dayOfMonth;
	}

	public Integer getDayOfWeek() {
		return dayOfWeek;
	}

	public void setDayOfWeek(Integer dayOfWeek) {
		this.dayOfWeek = dayOfWeek;
	}

	public Integer getHourOfDay() {
		return hourOfDay;
	}

	public void setHourOfDay(Integer hourOfDay) {
		this.hourOfDay = hourOfDay;
	}

	public Integer getMinuteOfHour() {
		return minuteOfHour;
	}

	public void setMinuteOfHour(Integer minuteOfHour) {
		this.minuteOfHour = minuteOfHour;
	}

	public Integer getSecondOfMinute() {
		return secondOfMinute;
	}

	public void setSecondOfMinute(Integer secondOfMinute) {
		this.secondOfMinute = secondOfMinute;
	}

	public Integer getTotalDayOfMonth() {
		return totalDayOfMonth;
	}

	public void setTotalDayOfMonth(Integer totalDayOfMonth) {
		this.totalDayOfMonth = totalDayOfMonth;
	}

	public Boolean getIsLeapYear() {
		return isLeapYear;
	}

	public void setIsLeapYear(Boolean isLeapYear) {
		this.isLeapYear = isLeapYear;
	}

}
